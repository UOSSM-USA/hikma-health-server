export type Language = keyof typeof translations;

export const supportedLanguages: Language[] = ["en", "ar"];

export function getTranslation(
  key: TranslationKey,
  language: Language = "en"
): string {
  const translation = translations[language]?.[key];
  if (typeof translation === "string") {
    return translation;
  }
  const fallback = translations.en[key];
  if (typeof fallback === "string") {
    return fallback;
  }
  // Fallback to key name when translation is missing or nested object was requested.
  return String(key);
}

/**
 * Helper to get nested translation values
 * Usage: t("nav.dashboard") -> "Dashboard" or "لوحة التحكم"
 */
export function t(
  path: string,
  language: Language = "en"
): string {
  const keys = path.split(".");
  let value: any = translations[language];

  for (const key of keys) {
    value = value?.[key];
    if (value === undefined) {
      // Fallback to English
      value = translations.en;
      for (const fallbackKey of keys) {
        value = value?.[fallbackKey];
        if (value === undefined) {
          return path; // Return path if translation not found
        }
      }
      break;
    }
  }

  return typeof value === "string" ? value : path;
}

